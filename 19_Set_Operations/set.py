# Create the sets
set1 = {1, 2, 3, 4}
set2 = {3, 4, 5, 6}

# Perform set operations
union = set1.union(set2)
intersection = set1.intersection(set2)
difference = set1.difference(set2)

# Print the results
print(f"Union: {union}")
print(f"Intersection: {intersection}")
print(f"Difference: {difference}")
